// Operadores lógicos e incrementos

/*
  Atribuição: =
  Comparação: ==
  Diferença: !=
  Menor que: <
  Menor ou igual que: <=
  Maior que: >
  Maior ou igual que: >=

  // Para Operações lógicas
  /*
    NOT: !
    OR: ||
    AND: &&

    NOT --> Inverte a lógica.
    Ex:
    var isDriver = false
    !isDriver passa agora a ser == true

    OR --> Uma ou outra condição precisa ser verdadeira.
    Ex:
    var isDriver = true
    var isStudent = false
    let result = isDriver || isStudent // true

    AND --> As duas condições precisam ser verdadeiras.
    Ex:
    var isDriver = true
    var isStudent = false
    let result = isDriver && isStudent // false
  */

*/

/*
  Incrementos:

  Operador de Incremento: +

    Ex:
    var n = 10
    n += 2
    // n agora é == 12

  Operador de Decremento: -

    Ex:
    var n = 10
    n -= 4
    // n agora é == 8

    
*/

